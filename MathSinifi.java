public class MathSinifi {
    public static void main(String[] args) {
        // .ceil(x) -> bir büyük tam sayıya yuvarlar
        // .cos(x) -> cos değerini döner
        // .sin(x) -> sin değerini döner
        // .floor(x) -> bir küçük tam sayıya yuvarlar
        // .max(x,y) -> büyük sayıyı döner
        // .min(x,y) -> küçük sayıyı döner
        // .pow(x,y) -> x üssü y döner
        // .random() -> 1-0 arası random değer
        // .round(x) -> yakın olan tam sayıyı döner
        // .abs(x) -> mutlak değer
        System.out.println(Math.sqrt(16));
    }
}
